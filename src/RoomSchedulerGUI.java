    public void fillDateCancel() {
        ArrayList<Date> dates = new ArrayList<>();
        ArrayList<ReservationEntry> reserve = ReservationQueries.getReservationsByFaculty(cmb_faculty_name.getSelectedItem().toString());
        for (int i = 0; i < reserve.size(); i++) {
            dates.add(reserve.get(i).getDate());
        }
        
        ArrayList <WaitlistEntry> waitlist = WaitlistQueries.getWaitlistByFaculty(cmb_faculty_name.getSelectedItem().toString());
        for (int g = 0; g < waitlist.size(); g++) {
            dates.add(waitlist.get(g).getDate());
        }
        cmb_date.setModel(new javax.swing.DefaultComboBoxModel(dates.toArray()));
    }
