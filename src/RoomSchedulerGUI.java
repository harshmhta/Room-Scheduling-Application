    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        // TODO add your handling code here:
        //Connection conn = null;
        //PreparedStatement ps = null;
        //ResultSet rs = null;

        try {

            
            String facultyName = (String) reserveFacultyComboBox.getSelectedItem();
            int seats = Integer.parseInt(reserveSeatsTextField.getText().trim());
            Date dd = Date.valueOf(reserveDateComboBox.getSelectedItem().toString());
            
            if(ReservationQueries.hasReserve(dd, facultyName)){
            
                lbl_reserve_message.setText(facultyName+ " Reservation could not made, existing reservation for the date!");
                return;
            }

            boolean result = ReservationQueries.addReservationEntry(facultyName, dd, seats);

            if (result == false) {

                lbl_reserve_message.setText("Reservation could not made, added to whitelist");
                WaitlistQueries.addWaitlistEntry(facultyName, dd, seats);
                fillWaitlist();

            } else {

                lbl_reserve_message.setText("Reervation has been made");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }


    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

