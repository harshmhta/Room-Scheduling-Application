    private void btn_drop_roomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_drop_roomActionPerformed
        String room = cmb_drop_room_name.getSelectedItem().toString();
        RoomQueries.dropRoom(room);
       
        
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservedByRoom(room);
        
        String message = "";
        
        for(int x = 0; x < reservations.size(); x++){
        
            WaitlistQueries.addWaitlistEntry(reservations.get(x).getFaculty(), reservations.get(x).getDate(), reservations.get(x).getSeats());
            message += reservations.get(x).getFaculty()+" "+ reservations.get(x).getDate()+" is moved to wait list \n";
        }

        
         ReservationQueries.deleteReservation(room);
         
         
        lbl_drop_room_message.setText("Room dropped!");
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        boolean removed = false;
        for (int i = 0; i < waitlist.size(); i++) {
            String faculty = waitlist.get(i).getFaculty();
            Date date = waitlist.get(i).getDate();
            int seats = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);
            if (result) {
                removed = true;
                
                message += faculty+" Reserved  Room"+ReservationQueries.roomReserved()+"  Date " +date;
                ReservationQueries.setroomReserved();
                
                WaitlistQueries.deleteWaitlistEntry(faculty, date);
                
                lbl_drop_room_message.setText("Room dropped and a room was reserved for a faculty!");
            }
        }
        if (removed == false) {
            lbl_drop_room_message.setText("Room Dropped!");
        }
        
        JOptionPane.showMessageDialog(rootPane, message);
        
        fillRooms();
    }//GEN-LAST:event_btn_drop_roomActionPerformed

