    private void btn_cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cancelActionPerformed
        String faculty = cmb_faculty_name.getSelectedItem().toString();
        Date date = Date.valueOf(cmb_date.getSelectedItem().toString());
        ReservationQueries.cancelReservation(faculty, date);
        WaitlistQueries.cancelWaitlistEntry(faculty, date);

        lbl_cancel_reserve_message.setText("Reservation Cancelled!");
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        for (int i = 0; i < waitlist.size(); i++) {
            faculty = waitlist.get(i).getFaculty();
            date = waitlist.get(i).getDate();
            int seats = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);
            if (result) {
                WaitlistQueries.deleteWaitlistEntry(faculty, date);

                lbl_cancel_reserve_message.setText("Reservation cancelled! and a room was reserved for a faculty!");
            }
        }
    }//GEN-LAST:event_btn_cancelActionPerformed

