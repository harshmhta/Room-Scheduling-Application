    private void btn_add_new_roomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_new_roomActionPerformed
        // TODO add your handling code here:
        String room_name = txt_room_name.getText();
        int room_number = Integer.parseInt(txt_number_of_rooms.getText());
        String message = "";

        boolean added = RoomQueries.addRoom(room_name, room_number);

        if (added) {
            lbl_add_room_message.setText("New Room Added");

            ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
            
            System.out.println("Waitlist size = "+waitlist.size());
            int count = 1;
            for (int i = 0; i < waitlist.size(); i++) {
                String faculty = waitlist.get(i).getFaculty();
                Date date = waitlist.get(i).getDate();
                int number_seats = waitlist.get(i).getSeats();
                
                System.out.println("Faculty "+faculty+" Date "+date+" Number of rooms "+number_seats);
                boolean result = ReservationQueries.addReservationEntry(faculty, date, number_seats);
                
                if (result) {
                    WaitlistQueries.deleteWaitlistEntry(faculty, date);
                    message += ReservationQueries.roomReserved()+" Reserved for "+faculty+" Date "+date+" Seats "+number_seats+"\n";
                    ReservationQueries.setroomReserved();
                    lbl_add_room_message.setText("["+count+"] This room was reserved for a faculty!");
                    
                    count++;
                }else{
                
                    System.out.println("Failed.."+date+" "+room_number);
                }
            }

            fillRooms();
        } else {
            lbl_add_room_message.setText("Room could not be added");
        }


        JOptionPane.showMessageDialog(rootPane, message);
    }//GEN-LAST:event_btn_add_new_roomActionPerformed

